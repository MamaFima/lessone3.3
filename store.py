store3.add_item("туфли черные", 5000)
store3.add_item("туфли белые", 5500)
store3.add_item("туфли коричневые", 4500)

print(f"Стоимость винограда до наценки: {store1.get_price('виноград')}")

store1.update_price("виноград", 220)
print(f"Стоимость винограда после наценки: {store1.get_price('виноград')}")

